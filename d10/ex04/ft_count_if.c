int	ft_count_if(char **tab, int (*f) (char*))
{
	int i;
	int num;

	i = 0;
	num = 0;
	while (*tab[i] != '\0' || *tab[i] != '0')
	{
		while (tab[i][num] != '\0' || tab[i][num] != '0')
		{
			if (f(tab[i][num]) == 1)
				return (1);
			num++;
		}
		i++;
	}
	return (num);
}
