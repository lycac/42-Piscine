int	match(char *s1, char *s2)
{
	while (*s2 == '*' && (*s1 != '\0' && *s2 != '\0'))
		s2++;
	while (*s2 != '*' && (*s1 != '\0' && *s2 != '\0'))
	{
		while (*s1 != *s2 && (*s1 != '\0' && *s2 != '\0'))
			s1++;
		if (*s1 == '\0' && *s2 == '\0')
			return (1);
		else
		{
			s2++;
			return (match(s1, s2));
		}
	}
	while (*s2 != '*' && (*s1 != '\0' && *s2 != '\0'))
	{
		while (*s2 != '*' && (*s1 != '\0' && *s2 != '\0'))
		{
			s1++;
			s2++;
		}
		if (*s2 == '*')
			return (1);
	}
}
